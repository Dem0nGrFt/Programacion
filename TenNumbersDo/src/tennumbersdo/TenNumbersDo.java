package tennumbersdo;

/**
 * Clase que representa a 10 numeros
 * 
 * @author aitor.martinezparente
 */
public class TenNumbersDo {

    /**
     * Inicia la ejecucion
     * 
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int n = 0;
        
        do {            
            n++;
            System.out.println(n);
        } while (n < 10);
        
    }
    
}
