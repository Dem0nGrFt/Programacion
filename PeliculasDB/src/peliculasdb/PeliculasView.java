    private void updateCombo() {
        String sql = "SELECT title FROM peliculas";
        try (Connection c = DriverManager.getConnection(url)) {
            try (Statement st = c.createStatement(); ResultSet rst = st.executeQuery(sql)) {
                while (rst.next()) {
                    cmbxPeliculas.addItem(rst.getString("title"));
                }
            }
        } catch (SQLException e) {
            System.out.println("A conexión co servidor de bases de datos non se puido establecer");
        }
    }

