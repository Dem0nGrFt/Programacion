    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        String tituloSeleccionado = (String) cmbxPeliculas.getSelectedItem();
        String sql = "DELETE FROM peliculas WHERE title = ?";

        try (Connection c = DriverManager.getConnection(url); PreparedStatement pst = c.prepareStatement(sql)) {
            pst.setString(1, tituloSeleccionado);
            pst.executeUpdate();
            cmbxPeliculas.removeItem(tituloSeleccionado);
        } catch (SQLException e) {

        }
    }//GEN-LAST:event_btnBorrarActionPerformed

