package tennumbers;

/**
 * Clase que representa a 10 numeros
 * 
 * @author aitor.martinezparente
 */
public class TenNumbers {

    /**
     * Inicia la ejecucion 
     * 
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int n = 0;

        while (n < 10) {
            n++;
            System.out.println(n);
        }
    }
    
}
